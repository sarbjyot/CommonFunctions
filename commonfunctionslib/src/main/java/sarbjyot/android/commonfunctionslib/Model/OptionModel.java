public class OptionModel {

    private String tabName;
    private int tabSelectedImage;
    private int tabUnSelectedImage;


    public String getTabName() {
        return tabName;
    }

    public void setTabName(String tabName) {
        this.tabName = tabName;
    }

    public int getTabSelectedImage() {
        return tabSelectedImage;
    }

    public void setTabSelectedImage(int tabSelectedImage) {
        this.tabSelectedImage = tabSelectedImage;
    }

    public int getTabUnSelectedImage() {
        return tabUnSelectedImage;
    }

    public void setTabUnSelectedImage(int tabUnSelectedImage) {
        this.tabUnSelectedImage = tabUnSelectedImage;
    }

}
